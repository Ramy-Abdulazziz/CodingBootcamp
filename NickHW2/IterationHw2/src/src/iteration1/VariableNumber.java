package iteration1;

public class VariableNumber {

	public static void main(String[] args) {
		for (

				int assFace = 1; assFace < 1000; assFace++) {

			if (assFace % 3 == 0) {

				System.out.println(assFace);
			} else if (assFace % 5 == 0) {
				System.out.println(assFace);
			}

		}

	}

}
